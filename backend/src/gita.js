export function findBestVerse(emotions) {
  // Score each verse based on emotion overlap
  let bestMatch = null;
  let bestScore = 0;
  const matchedVerses = [];

  for (const verse of gitaVerses) {
    let score = 0;
    for (const emotion of emotions) {
      if (verse.emotions.includes(emotion)) score++;
    }
    if (score > 0) {
      matchedVerses.push({ verse, score });
    }
    if (score > bestScore) {
      bestScore = score;
      bestMatch = verse;
    }
  }

  // If multiple verses match, pick a random one from the top matches for variety
  if (matchedVerses.length > 1) {
    matchedVerses.sort((a, b) => b.score - a.score);
    const topScore = matchedVerses[0].score;
    const topVerses = matchedVerses.filter(m => m.score === topScore).map(m => m.verse);
    if (topVerses.length > 1) {
      return topVerses[Math.floor(Math.random() * topVerses.length)];
    }
  }

  return bestMatch || gitaVerses[gitaVerses.length - 1];
}
